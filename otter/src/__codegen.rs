//! Internal helpers for `otter_codegen`. **Not part of the public API.**
//!
//! Everything here is `#[doc(hidden)]` and exists solely so that generated
//! code (which lives in the user's crate) can call into otter internals.

use std::ffi::c_int;

pub use crate::codec::{CodecError, Decoder, Encoder};
pub use crate::env::{Env, EnvKind};
pub use crate::sys::{
    NifEnv, NifEntry, NifFunc, NifResourceTypeInit, NifTerm, NIF_FUNC_DIRTY_CPU,
    NIF_FUNC_DIRTY_IO, NIF_MAJOR_VERSION, NIF_MIN_ERTS_VERSION, NIF_MINOR_VERSION,
    NIF_VM_VARIANT,
};
pub use crate::term::{RawTerm, TypedTerm};
pub use crate::types::Atom;

/// Value for `NifEntry.options` indicating `sizeof_resource_type_init` is set.
pub const NIF_ENTRY_OPTIONS: u32 = 1;

/// Metadata for a single NIF function, generated by `#[otter::nif]`.
pub struct NifMeta {
    pub name: &'static [u8],
    pub arity: u32,
    pub raw_fptr: unsafe extern "C" fn(*mut NifEnv, c_int, *const NifTerm) -> NifTerm,
    pub flags: u32,
}

impl NifMeta {
    /// Convert to a [`NifFunc`] for inclusion in a `NifEntry`.
    pub fn to_nif_func(&self) -> NifFunc {
        NifFunc {
            name: self.name.as_ptr() as *const std::ffi::c_char,
            arity: self.arity as std::ffi::c_uint,
            fptr: self.raw_fptr,
            flags: self.flags as std::ffi::c_uint,
        }
    }
}

/// Construct an [`Env`] from a raw pointer and stack-lifetime marker.
///
/// # Safety
///
/// Same requirements as `Env::new`.
#[inline]
pub unsafe fn new_env<'a>(
    marker: &'a (),
    env: *mut NifEnv,
    kind: EnvKind,
) -> Env<'a> {
    unsafe { Env::new(marker, env, kind) }
}

/// Wrap a raw `NifTerm` into a [`RawTerm`].
#[inline]
pub fn new_raw_term<'a>(env: Env<'a>, term: NifTerm) -> RawTerm<'a> {
    RawTerm::new(env, term)
}
